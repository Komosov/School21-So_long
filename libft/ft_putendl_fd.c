#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned long	l;

	l = 0;
	if (s && fd)
	{
		while (s[l])
			write(fd, &s[l++], 1);
		write(fd, "\n", 1);
	}
}
